    static void keyCallback(GLFWwindow* window, int key, int scancode, int action, int mods) {
        auto app = reinterpret_cast<HelloTriangleApplication*>(glfwGetWindowUserPointer(window));
        Camera* camera = &app->camera;

        bool keyAction = action == GLFW_PRESS || action == GLFW_REPEAT;

        if (key == GLFW_KEY_W) {
            camera->keys.forward = keyAction;
        }

        if (key == GLFW_KEY_S) {
            camera->keys.backward = keyAction;
        }

        if (key == GLFW_KEY_A) {
            camera->keys.left = keyAction;
        }

        if (key == GLFW_KEY_D) {
            camera->keys.right = keyAction;
        }
    }

    static void cursorPosCallback(GLFWwindow* window, double x, double y) {
        auto app = reinterpret_cast<HelloTriangleApplication*>(glfwGetWindowUserPointer(window));
        app->camera.yaw += x / 16;
        app->camera.pitch -= y / 16;

        app->camera.updateDirection();

        glfwSetCursorPos(window, 0, 0);
    }

